% ================================================================
% Функция hsap_H_and_HH – вычисление спектральных портретов методом
% итерационных уточнений и методом Малышева
% ------------------------------------------------
% Входные данные:
%   a, b – интервал по параметру λ
%   A    – матрица
%   e1,e2 – точки сетки для построения графиков
%   m1,m2 – нормы решений (итерации / метод Малышева)
%   m3,m4 – нормы разности решений
%   s1,s2,s3 – количество итераций
% ================================================================
function [e1,m1,m2,m3,m4,s1,s2,s3] = hsap_H_and_HH(a,b,A)

    % -----------------------------
    % Начальные установки
    % -----------------------------
    r1_val = a;        % Точка отсчета
    h = 0.01;         % Шаг по λ
    i = 1;            % Счетчик цикла
    l = 0;            % Флаг проекторных вычислений
    w_max = 1e10;      % Условие отсутствия сходимости решения
    u_max = 1e10;     % Порог для остановки дихотомии
    ip = 1e-15;       % Точность
    [n,~] = size(A);  % Размерность матрицы
    I = eye(n);       % Единичная матрица

    m0 = round(log2(- (1 + w_max) * log(ip / ((2 + 2 * ip) * sqrt(w_max)))));% Оценка максимального числа итераций для построения проектора

    % ================================================================
    % Первый цикл – Комбинированный метод
    % ================================================================
    while (r1_val < b)
        skip_step = false; 
        
        % Вычисление проектора выполняется только если l == 0
        if (l == 0)
            A0 = A / r1_val;
            B0 = I;
            p  = 2 * I;   % Начальное приближение проектора
            d  = 0;       % Счетчик внутренних итераций
            l  = 1;       % Устанавливаем флаг, что проектор в работе
            
            % Внутренний цикл вычисления проектора
            while max(abs(p^2 - p), [], 'all') > ip * max(abs(p), [], 'all') && d <= m0/3 
                d = d + 1;
                for i1 = 1:3
                    S_temp = [-B0; A0];
                    [Q_mat, ~] = qr(S_temp);
                    QQ     = Q_mat';
                    Q1_sub = QQ(n+1:2*n, 1:n);
                    Q2_sub = QQ(n+1:2*n, n+1:2*n);
                    A0     = Q1_sub * A0;
                    B0     = Q2_sub * B0;
                end

               if (max(cond(A0 - B0), cond(A0 + B0)) > u_max)
                    skip_step = true;
                    break; 
                end
                p = -inv(A0 - B0) * B0; 
            end
            
            % Если проектор не сошелся
            if skip_step || isempty(p) || any(isnan(p(:)))
                e1(i) = r1_val;
                  m1(i)=NaN;
                  s1(i)=NaN;
                  s2(i)=NaN;
                l = 0; % Сброс флага для следующей попытки
                i = i + 1;
                r1_val = r1_val + h;
                fprintf('Предупреждение: проектор не сошелся при r = %f\n', r1_val);
                continue 
            end

            % --- Разделение пространства (SVD разложение проектора) ---
            t = round(trace(p)); % Размерность подпространства
            
            [U1, ~, ~] = svd(p);
            u1 = U1(:, 1:t);     % Базис подпространства внутри круга

            [U2, ~, ~] = svd(I - p);
            u2 = U2(:, 1:n-t);   % Базис подпространства вне круга

            % Матрица перехода к блочно-диагональному виду
            T = [u1, u2];
            in_T = inv(T);

            V = in_T * A * T;
            AA = V(1:t, 1:t);           % Блок "внутри"
            BB = V(t+1:n, t+1:n);       % Блок "снаружи"

            % Метрические матрицы
            W = T' * T;
            w11 = W(1:t, 1:t);
            w22 = W(t+1:n, t+1:n);

            inv_BB = inv(BB);
        end

        % ------------------------------------------------------------
        % Вычисление первой компоненты решения методом ускоренного уточнения с использованием возведения матрицы в степень для ускорения сходимости и контроля по точности и норме решения.
        % ------------------------------------------------------------
        aa  = AA/r1_val;

        x0 = w11; % Начальное приближение для первой компоненты решения (матрицы  x1).
        x1 = x0 + (aa')*x0*aa;% Первое итерационное уточнение решения  x1
        s  = 0; % Инициализация счётчика вложенных итераций уточнения.

        while (t > 0 && max(abs(x1 - x0), [], 'all') > ip && max(abs(x1), [], 'all') < w_max && s < 10)
            s = s + 1; % Увеличение счётчика итераций.
            for j=1:3 % Внутренний цикл
                aa = aa*aa; % Возведение aa в квадрат
                x0 = x1; % Обновление предыдущего приближения.
                x1 = x0 + aa'*x0*aa; % Следующее уточнение приближённого решения  x1.
            end
        end
        % Подсчет количества итерации, используемых в цикле
        s1(i) = s*3;
     aa1 = AA/r1_val; % Это подготавливает матрицу к итерационному процессу уточнения.
       %Вычисление решения в следующей итерации
        xx1 = w11 + (aa1')*x1*aa1;
        
        
      % Аналогичным образом вычисляется второе решение y1.
        bb  = r1_val*inv_BB;
        y0  = bb'*w22*bb;
        y1  = y0 + bb'*y0*bb;
        s   = 0;

        while (t < n && max(abs(y1 - y0), [], 'all') > ip && max(abs(y1), [], 'all') < w_max && s < 10)
            s = s + 1;
            for j=1:3
                bb = bb*bb;
                y0 = y1;
                y1 = y0 + bb'*y0*bb;
            end
        end
        % Подсчет количества итерации, используемых в цикле
        s2(i) = s*3;
        
        % Вычисление решения в следующей итерации
        yy1 = (r1_val^2)*(inv_BB'*w22*inv_BB) + (r1_val^2)*(inv_BB'*y1*inv_BB);
        
        % Два приближённых решения дискретного уравнения Ляпунова объединяются для получения приближённого решения обобщённого уравнения Ляпунова.
        mm1 = zeros(t,n-t);
        zz  = [x1,mm1;mm1',y1];
        H1  = (in_T)'*zz*in_T;
        zzz = [xx1,mm1;mm1',yy1];
        HH1 = (in_T)'*zzz*in_T;

         %Расчёт нормы решения и проверка предела
        m1(i) =  max(abs(H1), [], 'all');
        m3(i) =  max(abs(H1-HH1), [], 'all');
        if (m1(i) >= w_max )
            m1(i) = NaN;
            l = 0;
        end

        % Подготовка следующей итерации
        e1(i) = r1_val;
        i = i + 1;
        r1_val = r1_val + h;
    end

    % ================================================================
    % Второй цикл – метод Малышева
    % ================================================================
    clear {r1_val,i,A0,B0,d,S_temp,Q_mat,inv_su}
    i = 1;
    r1_val = a;

    while (r1_val < b)
        skip_step = false; 
        A0 = A' / r1_val;
        B0 = I;
        p  = 2 * I;
        d  = 0;
        
        % Внутренний цикл уточнения проектора
        while (max(abs(p * p - p), [], 'all') > ip * max(abs(p), [], 'all') && d <= m0/3 )
            d = d + 1;
            for i1 = 1:3
                S_temp = [-B0; A0];
                [Q_mat, ~] = qr(S_temp);
                QQ     = Q_mat';
                Q1_sub = QQ(n+1:2*n, 1:n);
                Q2_sub = QQ(n+1:2*n, n+1:2*n);
                A0     = Q1_sub * A0;
                B0     = Q2_sub * B0;
            end

            % Проверка обусловленности
            if (max(cond(A0 - B0), cond(A0 + B0)) > u_max)
                skip_step = true;
                break; 
            end
            
            p = -inv(A0 - B0) * B0; 
        end
        
        % Обработка ошибок сходимости
        if skip_step
            e2(i) = r1_val;
            m2(i)=NaN;
            m4(i)=NaN;
            s3(i)=NaN;
            i     = i + 1;
            r1_val = r1_val + h;
            fprintf('Внимание: не удалось получить проектор при r = %f\n', r1_val);
            continue 
        end
        % Подсчет количества итерации, используемых в цикле
        s3(i) = d*3;
        
        % Сохранение текущих матриц
       

        % Формирование решения по Малышеву
        inv_su = inv(B0+A0);
        H2 = inv_su*inv_su';
        m2(i) = max(abs(H2), [], 'all');

       % Вычисление решения HH2 в следующей итерации
       S_temp = [-B0; A0];
                [Q_mat, ~] = qr(S_temp);
                QQ     = Q_mat';
                Q1_sub = QQ(n+1:2*n, 1:n);
                Q2_sub = QQ(n+1:2*n, n+1:2*n);
                A1     = Q1_sub * A0;
                B1     = Q2_sub * B0;

        inv_sw = inv(B1+A1);
        HH2 = inv_sw*inv_sw';
        m4(i) =max(abs(H2-HH2), [], 'all');

        e2(i) = r1_val;
        
        % Увеличиваем счётчик
        i = i + 1;
        r1_val = r1_val+h;
    end



